package com.green.day3.ch4;

public class IfElseIf {
    public static void main(String[] args) {
        if (1 == 1) {
            System.out.println("1");
        } else if (2 == 2) {
            System.out.println("2");
        } else if (3 == 3) {
            System.out.println("3");
        } else {
            System.out.println("else");
        }
    }
}